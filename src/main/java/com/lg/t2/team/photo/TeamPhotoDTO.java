public class TeamPhotoDTO {
	
	private long dexNum;
	private int tNum;
	private int kind;
	private String fURL;
	
	public long getDexNum() {
		return dexNum;
	}
	public void setDexNum(long dexNum) {
		this.dexNum = dexNum;
	}
	public int gettNum() {
		return tNum;
	}
	public void settNum(int tNum) {
		this.tNum = tNum;
	}
	public int getKind() {
		return kind;
	}
	public void setKind(int kind) {
		this.kind = kind;
	}
	public String getfURL() {
		return fURL;
	}
	public void setfURL(String fURL) {
		this.fURL = fURL;
	}
	
}
